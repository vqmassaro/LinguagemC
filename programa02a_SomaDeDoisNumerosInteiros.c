int main()
{
    //METODO 01
    printf("Metodo 1:\n");
    int soma = 1+1;
    printf("1 + 1 = %d\n\n", soma);

    //METODO 02
    printf("Metodo 2:\n");
    int numero1 = 10;
    int numero2 = 20;

    soma = numero1 + numero2;

    printf("%d + %d = %d \n\n", numero1, numero2, soma);

    //METODO 03
    printf("Metodo 3:\n");
    printf("%d - %d = %d \n\n", soma, numero1, (soma-numero1));
}

